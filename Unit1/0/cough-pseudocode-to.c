/**
 * cough.c
 *
 *
 * Coughs three times.
 *
 * Demonstrates converting from Scratch to pseudocode to c code
 * Scratch code can be found at https://scratch.mit.edu/projects/26329122/
 */
       
       
// pseudocode first

// cough once
// cough again
// one more cough after that 

// then, translate to C code, with the psuedocode left in as comments

#include <stdio.h>

int main(void)
{
    // cough once
    printf("cough\n");
    // cough again
    printf("cough\n");
    // one more cough after that 
    printf("cough\n");
}